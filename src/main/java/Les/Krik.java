package Les;

public class Krik {

    String malina;
    String cucoriedka;
    int pocet;

    public String getMalina() {
        return malina;
    }

    public void setMalina(String malina) {
        this.malina = malina;
    }

    public String getCucoriedka() {
        return cucoriedka;
    }

    public void setCucoriedka(String cucoriedka) {
        this.cucoriedka = cucoriedka;
    }

    public int getPocet() {
        return pocet;
    }

    public void setPocet(int pocet) {
        this.pocet = pocet;
    }

    public Krik(String malina, String cucoriedka, int pocet) {
        this.malina = malina;
        this.cucoriedka = cucoriedka;
        this.pocet = pocet;


    }
}
